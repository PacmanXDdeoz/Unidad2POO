class Suma{
    int numerador;
    int sumar;
    Suma(){
    }
    public int getNumerador() {
        return numerador;
    }
    public void setNumerador(int numerador) {
        this.numerador = numerador;
    }
    public int getSumar() {
        return sumar;
    }
    public void setSumar(int sumar) {
        this.sumar = sumar;
    }
    
}