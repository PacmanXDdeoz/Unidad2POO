class Modulo{
    int numerador;
    int divisor;
    Modulo(){
    }
    public int getNumerador() {
        return numerador;
    }
    public void setNumerador(int numerador) {
        this.numerador = numerador;
    }
    public int getDivisor() {
        return divisor;
    }
    public void setDivisor(int divisor) {
        this.divisor = divisor;
    }
    
}